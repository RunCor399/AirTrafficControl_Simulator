package model;

/**
 * An Interface that define a position of an object in a bidimensional space.
 */

public interface Position2D {

    /**
     * this method return the coordinate X of an object.
     * 
     * @return double
     */
    Double getX();

    /**
     * this method return the coordinate Y of an object.
     * 
     * @return double
     */
    Double getY();
}
